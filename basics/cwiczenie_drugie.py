a = int(input("Podaj długość podstawy "))
h = int(input("Podaj wysokość trójkąta "))
pole_trojkata = a * h / 2

print(f"Pole trójkąta o długości podstawy {a} i wysokości {h} wynosi: {pole_trojkata}")
